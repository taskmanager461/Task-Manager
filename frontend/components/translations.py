def translate(language: str, key: str, **kwargs: str) -> str:
    canonical = language if language in TRANSLATIONS else "en"
    base = TRANSLATIONS["en"]
    local = TRANSLATIONS.get(canonical, {})
    template = local.get(key, base.get(key, key))
    try:
        return template.format(**kwargs)
    except Exception:
        return template
